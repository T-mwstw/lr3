package User;

public class User {
    public String name;
    public String poroda;
    public String age;
    public String color;
    public String warning;

    public User(String name, String poroda, String age, String color, String warning){
        this.name = name;
        this.poroda = poroda;
        this.age = age;
        this.color= color;
        this.warning = warning;
    }
}
